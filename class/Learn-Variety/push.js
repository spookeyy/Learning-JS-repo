//array destructive methods
//.push  -  adds an item to the end of an array
//.unshift - adds an item to the beginning of an array

const superheroes = ["Batman", "Superman", "Spiderman", "Hulk", "Wolverine", "Cyclops"]

//push
superheroes.push("Hawkman")
console.log(superheroes);

//unshift
const cities = ["London", "Paris", "New York", "Sydney"]
cities.unshift("Nairobi", "Kiambu", "Kampala")
console.log(cities)
